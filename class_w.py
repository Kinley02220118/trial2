class Dog:
    def __init__(self, name, colour, age):
        self.name = name
        self.colour = colour
        self.age = age

dawa = Dog('dawa', 'blue', 2)
nima = Dog('nima', 'black', 1)
blacky = Dog('blacky', 'yellow', 3)

print(dawa.name)
print(dawa.colour)
print(dawa.age)
print(nima.name)
print(nima.colour)
print(nima.age)
print(blacky.name)
print(blacky.colour)
print(blacky.age)
